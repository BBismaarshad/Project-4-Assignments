def main():
    # Prompt the user to enter two numbers
    num1 = float(input("Enter the first number: "))
    num2 = float(input("Enter the second number: "))
    
    # Calculate the average
    average = (num1 + num2) / 2
    
    # Print the result
    print("The average is:", average)

if __name__ == '__main__':
    main()